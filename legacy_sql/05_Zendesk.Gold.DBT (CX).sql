-- ============================================================
-- VIEW: Ticket Field Options (lê Bronze para expor opções de campos custom)
-- ============================================================
CREATE OR REPLACE VIEW CX.GOLD.ZENDESK_TICKET_FIELD_OPTIONS AS
SELECT DISTINCT
    tf.RAW_DATA:id::BIGINT AS FIELD_ID,
    tf.RAW_DATA:title::STRING AS FIELD_TITLE,
    o.value:value::STRING AS OPTION_VALUE,
    o.value:name::STRING AS OPTION_LABEL
FROM WILD_CARD.BRONZE.ZENDESK_TICKET_FIELDS tf,
    LATERAL FLATTEN(input => PARSE_JSON(tf.RAW_DATA:custom_field_options)) o
WHERE tf.RAW_DATA:custom_field_options IS NOT NULL;

