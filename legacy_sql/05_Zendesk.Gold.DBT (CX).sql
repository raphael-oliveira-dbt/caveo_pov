    TICKET_ID = src.TICKET_ID, VALUE = src.VALUE,
    AUTHOR_ID = src.AUTHOR_ID, CREATED_AT = src.CREATED_AT,
    DATALAKE_UPDATED_AT = src.DATALAKE_UPDATED_AT, _SNOWFLAKE_LOADED_AT = src._SNOWFLAKE_LOADED_AT
