CREATE OR REPLACE PROCEDURE CX.GOLD.ZENDESK_TICKETS_EVENTS()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN
  MERGE INTO CX.GOLD.ZENDESK_TICKETS_EVENTS tgt
  USING (
    SELECT ID, TICKET_ID, FIELD_NAME, VALUE, AUTHOR_ID, CREATED_AT, DATALAKE_UPDATED_AT, _SNOWFLAKE_LOADED_AT
