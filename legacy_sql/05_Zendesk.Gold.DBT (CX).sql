-- Masking policy para SUBJECT na Gold
CREATE OR REPLACE MASKING POLICY CX.GOLD.MASK_ZENDESK_TICKET_SUBJECT_PII
  AS (val STRING) RETURNS STRING ->
  CASE
    WHEN CURRENT_ROLE() IN ('ROLE_DATA_CX', 'ROLE_DOMAIN_CX_PII', 'ACCOUNTADMIN')
      THEN val
    ELSE LEFT(val, 4) || REPEAT('*', GREATEST(LENGTH(val) - 4, 0))
  END;

ALTER TABLE CX.GOLD.ZENDESK_TICKETS MODIFY COLUMN SUBJECT
  SET MASKING POLICY CX.GOLD.MASK_ZENDESK_TICKET_SUBJECT_PII;

