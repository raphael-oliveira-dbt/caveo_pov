  VALUES (src.ID, src.TICKET_ID, src.FIELD_NAME, src.VALUE, src.AUTHOR_ID, src.CREATED_AT, src.DATALAKE_UPDATED_AT, src._SNOWFLAKE_LOADED_AT);

